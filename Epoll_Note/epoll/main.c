int main( int argc, char *argv[] )
{
	int iSrvfd;
	int iRet=0;
	//创建并初始化服务器对象
	if((iRet=SRV_init())<0)
	{
		goto err;
	}
	//服务流程初始化
	iSrvfd=SRV_creat_proc_server(IPADDR,PORT);
    if(iSrvfd<0)
    {
		printf("SRV_creat_proc_server error\n");
		iRet = iSrvfd;
		goto err1;
	}
	//处理客户端请求
	SRV_handle_client(iSrvfd);
	SRV_release();
	return 0;
err1:
	SRV_release();
err:
	return iRet;
}
